RUN_SIGNAL_BACKTEST_PARAMS = {
    "type": "object",
    "properties": {
        "code": {
            "type": "string",
            "description": (
                "Python source defining `def compute(df: pd.DataFrame) -> pd.Series`. "
                "Only `pd` and `np` are available, no imports. Must be causal: only use "
                "rows up to and including the current index, never future rows."
            ),
        },
        "cost_bps": {
            "type": "number",
            "description": "Round-trip trading cost in basis points. Default 5.",
        },
    },
    "required": ["code"],
}


class _RunContext:
    """Mutable state threaded through one Coordinator.run() call."""

    def __init__(self) -> None:
        self.data_path: Path | None = None
        self.data_df = None
        self.cache_meta: dict[str, Any] | None = None
        self.last_metrics: dict[str, float] | None = None
        self.signal_code: str | None = None
        self.warnings: list[str] = []


def _build_registry(ctx: _RunContext, run) -> SkillRegistry:
    registry = SkillRegistry()

    def _fetch_ohlcv(symbol: str, timeframe: str, start: str, end: str) -> dict:
        data_path, data_df, cache_meta = fetch_ohlcv(symbol, timeframe, start, end)
        ctx.data_path, ctx.data_df, ctx.cache_meta = data_path, data_df, cache_meta

        if cache_meta.get("source") == SYNTHETIC_FALLBACK_SOURCE:
            ctx.warnings.append(
                "DATA IS SYNTHETIC, NOT REAL MARKET DATA. Real data fetch failed "
                f"({cache_meta.get('fallback_reason')}); every downstream metric is "
                "meaningless for real trading decisions."
            )
        elif cache_meta.get("source") == "unknown_legacy_cache":
            ctx.warnings.append(
                "Cached data has no provenance metadata (cached before source tracking "
                "existed). Delete data_cache/ and re-run to confirm real vs synthetic."
            )

        return {
            "rows": len(data_df),
            "start": str(data_df["timestamp"].iloc[0]) if len(data_df) else None,
            "end": str(data_df["timestamp"].iloc[-1]) if len(data_df) else None,
            "source": cache_meta.get("source"),
            "cache_hit": cache_meta.get("cache_hit"),
        }

    def _run_signal_backtest(code: str, cost_bps: float = DEFAULT_COST_BPS) -> dict:
        if ctx.data_df is None:
            return {"error": "no market data loaded yet - call fetch_ohlcv first"}

        signal = run_signal_code(code, ctx.data_df)
        backtest = run_vectorized_backtest(ctx.data_df, signal, cost_bps=cost_bps)

        ctx.signal_code = code
        ctx.last_metrics = backtest.metrics
        run.save_code("signal.py", SIGNAL_FILE_HEADER + code + SIGNAL_FILE_HARNESS)
        run.save_json("backtest_result.json", backtest.to_json_dict())
        save_equity_curve_plot(backtest.equity_curve, run.run_dir / "equity_curve.png")
        save_drawdown_plot(backtest.drawdown, run.run_dir / "drawdown.png")

        new_warnings = sanity_check_metrics(backtest.metrics)
        ctx.warnings.extend(new_warnings)

        return {**backtest.metrics, "warnings": new_warnings}

    registry.register(
        Skill("fetch_ohlcv", "Fetch and cache OHLCV market data.", FETCH_OHLCV_PARAMS, _fetch_ohlcv)
    )
    registry.register(
        Skill(
            "run_signal_backtest",
            "Run signal code against the fetched data and backtest it.",
            RUN_SIGNAL_BACKTEST_PARAMS,
            _run_signal_backtest,
        )
    )
    return registry


def _message_to_dict(message: Any) -> dict:
    entry: dict[str, Any] = {"role": getattr(message, "role", "assistant"), "content": getattr(message, "content", None)}
    tool_calls = getattr(message, "tool_calls", None)
    if tool_calls:
        entry["tool_calls"] = [
            {
                "id": call.id,
                "type": "function",
                "function": {"name": call.function.name, "arguments": call.function.arguments},
            }
            for call in tool_calls
        ]
    return entry


class Coordinator:
    def __init__(
        self,
        model: str | None = None,
        run_store: ArtifactStore | None = None,
        llm: LLMClient | None = None,
    ):
        self.model = model or DEFAULT_MODEL
        self.run_store = run_store or ArtifactStore(RUNS_DIR)
        self.llm = llm or LLMClient(self.model)

    def run(self, user_request: str) -> RunResult:
        run = self.run_store.create_run(user_request)
        ctx = _RunContext()
        registry = _build_registry(ctx, run)

        messages: list[dict] = [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user_request},
        ]

        summary = ""
        for _ in range(MAX_STEPS):
            response = self.llm.chat(messages, tools=registry.schemas())
            message = response.choices[0].message
            messages.append(_message_to_dict(message))

            tool_calls = getattr(message, "tool_calls", None)
            if not tool_calls:
                summary = message.content or ""
                break

            for call in tool_calls:
                name = call.function.name
                try:
                    args = json.loads(call.function.arguments or "{}")
                except json.JSONDecodeError as exc:
                    result: Any = {"error": f"invalid tool arguments JSON: {exc}"}
                else:
                    try:
                        result = registry.execute(name, args)
                    except Exception as exc:
                        result = {"error": f"{type(exc).__name__}: {exc}"}
                run.log_step(name, args, result)
                messages.append(
                    {"role": "tool", "tool_call_id": call.id, "content": json.dumps(result, default=str)}
                )
        else:
            summary = "Reached the step limit before the model produced a final answer."
            ctx.warnings.append("Coordinator hit MAX_STEPS without a final natural-language answer.")

        config = {
            "hypothesis": user_request,
            "model": self.model,
            "data_path": str(ctx.data_path) if ctx.data_path else None,
            "cache": ctx.cache_meta,
        }
        run.save_config(config)

        metrics = ctx.last_metrics or {}
        data_hash = f"sha256:{file_sha256(ctx.data_path)}" if ctx.data_path else "sha256:none"
        code_path = run.run_dir / "signal.py"
        code_hash = f"sha256:{file_sha256(code_path)}" if code_path.exists() else "sha256:none"

        note = build_research_note(run.run_id, config, metrics, data_hash, ctx.warnings, summary)
        run.save_text("research_note.md", note)
        run.save_json("conversation.json", messages)
        run.finalize(
            data_hash=data_hash,
            code_hash=code_hash,
            warnings=ctx.warnings,
            model=self.model,
            conversation_log="conversation.json",
        )

        return RunResult(
            run_id=run.run_id,
            run_dir=run.run_dir,
            metrics=metrics,
            warnings=ctx.warnings,
            summary=summary,
        )
